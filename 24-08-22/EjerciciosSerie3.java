public class EjerciciosSerie3 { 
    public static void main(String[] args) {

        int n = 15;
        for(int i = 1; i <= n;i++) {
            System.out.print(i+" ");
        }
        System.out.println();
        for(int i = n;i >= 1;i-=2) {
            System.out.print(i+" ");
        }
        System.out.println();
    }
}
